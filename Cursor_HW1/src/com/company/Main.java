public class Main {

    public static void main(String[] args) {
        int num1 = 15, num2 = 7;
        int Add, Ded, Mult, Div;
        Add = num1 + num2;
        Ded = num1 - num2;
        Mult = num1 * num2;
        Div = num1 / num2;
        System.out.println(Add);
        System.out.println(Ded);
        System.out.println(Mult);
        System.out.println(Div);
    }
}
